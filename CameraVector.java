public class CameraVector {
    public float x, y, z; //x,y,z coordinates in the 3D space.
    
    /**
     * Instantiates Coordinates with specified coordinates.
     * @param x X-Coordinate
     * @param y Y-Coordinate
     * @param z Z-Coordinate
     */
    public CameraVector(int x, int y, int z){
        this.x=x;
        this.y=y;
        this.z=z;
    }
}
